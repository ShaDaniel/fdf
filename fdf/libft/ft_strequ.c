#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	size_t i;

	i = 0;
	if (s1 && s2)
	{
		while (s1[i] == s2[i] && s2[i])
			i++;
		if (s1[i] == s2[i])
			return (1);
		else
			return (0);
	}
	return (2);
}
